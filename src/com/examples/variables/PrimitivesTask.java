package com.examples.variables;

public class PrimitivesTask {
    public static void main(String[] args) {

        /* here we declaring all primitive datatypes with
         different values
         they are 8 datatypes in java
         1.integer (byte(1byte),short(2),int(4),long(8)(l//L)
         2.float(4)(f//F),double(8)
         3.char(2)
         4.boolean(true // false)
         */

        // integer datatypes
        byte value;
        value = 123;
        System.out.println("the byte value:" +value);

        short value1;
        value1 =12332;
        System.out.println("the short value:" +value1);

        int value2;
        value2 = 12324525;
        System.out.println("the integer value:" +value2);

        long value3;
        value3 = 121524534;
        System.out.println("the long value:" + value3);

        //float and double datatypes
        float number1;
        number1 = 122.3f;
        System.out.println("the float number:" +number1);

        double number2;
        number2 = 213123.22;
        System.out.println("the double number:" + number2);

        //char and boolean datatypes

        char symbol;
        symbol ='v';
        System.out.println("the char symbol:" +symbol);

        boolean type;
        type = true;
        System.out.println("the boolean is:" +type);


    }
}
